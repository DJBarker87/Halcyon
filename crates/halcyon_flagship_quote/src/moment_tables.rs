pub const MOMENT_SIGMA_MIN_S6: i64 = 50_000;
pub const MOMENT_SIGMA_MAX_S6: i64 = 1_000_000;
pub const MOMENT_N_SIGMA: usize = 64;

const CR_W: [[i32; 4]; WN] = {
    let mut out = [[0i32; 4]; WN];
    let scale: i64 = 1 << WS;
    let mut k = 0usize;
    while k < WN {
        let t = (k as i64) << (WS - 10);
        let t2 = t * t >> WS;
        let t3 = t2 * t >> WS;
        out[k][0] = ((-t + 2 * t2 - t3) / 2) as i32;
        out[k][1] = ((2 * scale - 5 * t2 + 3 * t3) / 2) as i32;
        out[k][2] = ((t + 4 * t2 - 3 * t3) / 2) as i32;
        out[k][3] = ((-t2 + t3) / 2) as i32;
        k += 1;
    }
    out
};

#[inline(always)]
fn cr_dot(w: &[i32; 4], p0: i64, p1: i64, p2: i64, p3: i64) -> i64 {
    (w[0] as i64 * p0 + w[1] as i64 * p1 + w[2] as i64 * p2 + w[3] as i64 * p3) >> WS
}

#[derive(Clone, Copy)]
pub struct MomentInterp {
    i0: i32,
    frac: usize,
}

#[inline(always)]
pub fn moment_interp(sigma_s6: i64) -> MomentInterp {
    let sigma = sigma_s6.clamp(MOMENT_SIGMA_MIN_S6, MOMENT_SIGMA_MAX_S6);
    let offset = sigma - MOMENT_SIGMA_MIN_S6;
    let ix = offset * MOMENT_N1;
    MomentInterp {
        i0: (ix / SIGMA_RANGE_S6).min(MOMENT_N_SIGMA as i64 - 2) as i32,
        frac: ((((ix % SIGMA_RANGE_S6) * WN as i64) / SIGMA_RANGE_S6) as usize).min(WN - 1),
    }
}

#[inline(always)]
pub fn moment_lookup_with_interp(table: &[i64; 64], interp: MomentInterp) -> i64 {
    let weight = &CR_W[interp.frac];
    let n = MOMENT_N_SIGMA as i32;
    let p0 = table[(interp.i0 - 1).clamp(0, n - 1) as usize];
    let p1 = table[interp.i0.clamp(0, n - 1) as usize];
    let p2 = table[(interp.i0 + 1).clamp(0, n - 1) as usize];
    let p3 = table[(interp.i0 + 2).clamp(0, n - 1) as usize];
    cr_dot(weight, p0, p1, p2, p3).clamp(-8 * S6, 8 * S6)
}

#[inline(always)]
pub fn moment_lookup(table: &[i64; 64], sigma_s6: i64) -> i64 {
    moment_lookup_with_interp(table, moment_interp(sigma_s6))
}
