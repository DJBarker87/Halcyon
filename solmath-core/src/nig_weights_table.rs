/// Look up one importance weight from a table via Catmull-Rom.
#[inline(always)]
fn iw_lookup(table: &[i32; 64], sigma_s6: i64) -> i64 {
    let s = sigma_s6.clamp(IW_SIGMA_LO, IW_SIGMA_LO + IW_SIGMA_RANGE);
    let off = s - IW_SIGMA_LO;
    let ix = off * IW_N1;
    let i0 = (ix / IW_SIGMA_RANGE).min(IW_N as i64 - 2) as i32;
    let wi = ((ix % IW_SIGMA_RANGE) / IW_FRAC_DIV) as usize;
    let w = &CR_W[if wi < WN { wi } else { WN - 1 }];
    let n = IW_N as i32;
    let p0 = table[(i0 - 1).clamp(0, n - 1) as usize] as i64;
    let p1 = table[i0.clamp(0, n - 1) as usize] as i64;
    let p2 = table[(i0 + 1).clamp(0, n - 1) as usize] as i64;
    let p3 = table[(i0 + 2).clamp(0, n - 1) as usize] as i64;
    cr_dot(w, p0, p1, p2, p3).clamp(0, S6)
}

/// Look up all 9 normalized importance weights for a given σ_common.
///
/// `sigma_s6`: σ_common at SCALE_6 (e.g. 200_000 for σ = 0.20).
/// Returns 9 weights at SCALE_6 summing to ~S6 (= 1.0).
///
/// ~900 CU total on SBF.
#[inline(always)]
pub fn nig_importance_weights_9(sigma_s6: i64) -> [i64; 9] {
    let mut w = [0i64; 9];
    for k in 0..9 {
        w[k] = iw_lookup(IW_TABLES[k], sigma_s6);
    }
    w
}
