/// Look up projected post-obs1 state via Catmull-Rom interpolation.
/// Returns (safe_state, knocked_state, first_hit, first_knock_in).
/// The returned FilterStates have at most N_PROJ_NODES active nodes,
/// ready for predict_state at obs-2 with no further projection needed.
pub fn obs1_projected_lookup(sigma_s6: i64) -> (FilterState, FilterState, i64, i64) {
    let mut safe = FilterState::default();
    let mut knocked = FilterState::default();
    let mut safe_active = 0usize;
    let mut knocked_active = 0usize;
    let mut k = 0usize;
    while k < N_PROJ_NODES {
        let sw = obs1_lookup(&OBS1_PROJ_SAFE_W[k], sigma_s6).max(0);
        if sw > 0 {
            safe.nodes[k] = FilterNode {
                c: obs1_lookup(&OBS1_PROJ_SAFE_C[k], sigma_s6),
                w: sw,
                mean_u: obs1_lookup(&OBS1_PROJ_SAFE_MU[k], sigma_s6),
                mean_v: obs1_lookup(&OBS1_PROJ_SAFE_MV[k], sigma_s6),
            };
            safe_active += 1;
        }
        let kw = obs1_lookup(&OBS1_PROJ_KNOCKED_W[k], sigma_s6).max(0);
        if kw > 0 {
            knocked.nodes[k] = FilterNode {
                c: obs1_lookup(&OBS1_PROJ_KNOCKED_C[k], sigma_s6),
                w: kw,
                mean_u: obs1_lookup(&OBS1_PROJ_KNOCKED_MU[k], sigma_s6),
                mean_v: obs1_lookup(&OBS1_PROJ_KNOCKED_MV[k], sigma_s6),
            };
            knocked_active += 1;
        }
        k += 1;
    }
    safe.n_active = safe_active;
    knocked.n_active = knocked_active;
    let fh = obs1_lookup(&OBS1_FIRST_HIT, sigma_s6).max(0);
    let fki = obs1_lookup(&OBS1_FIRST_KNOCK_IN, sigma_s6).max(0);
    (safe, knocked, fh, fki)
}

