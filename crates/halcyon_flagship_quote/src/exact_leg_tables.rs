pub const LOOKUP_SIGMA_MIN_S6: i64 = 50_000;
pub const LOOKUP_SIGMA_MAX_S6: i64 = 1_000_000;
pub const LOOKUP_N_SIGMA: usize = 64;

const CR_W: [[i32; 4]; WN] = {
    let mut out = [[0i32; 4]; WN];
    let scale: i64 = 1 << WS;
    let mut k = 0usize;
    while k < WN {
        let t = (k as i64) << (WS - 10);
        let t2 = t * t >> WS;
        let t3 = t2 * t >> WS;
        out[k][0] = ((-t + 2 * t2 - t3) / 2) as i32;
        out[k][1] = ((2 * scale - 5 * t2 + 3 * t3) / 2) as i32;
        out[k][2] = ((t + 4 * t2 - 3 * t3) / 2) as i32;
        out[k][3] = ((-t2 + t3) / 2) as i32;
        k += 1;
    }
    out
};

#[inline(always)]
fn cr_dot(w: &[i32; 4], p0: i64, p1: i64, p2: i64, p3: i64) -> i64 {
    (w[0] as i64 * p0 + w[1] as i64 * p1 + w[2] as i64 * p2 + w[3] as i64 * p3) >> WS
}

#[inline(always)]
pub fn lookup_table(table: &[i64; 64], sigma_s6: i64) -> i64 {
    let sigma = sigma_s6.clamp(LOOKUP_SIGMA_MIN_S6, LOOKUP_SIGMA_MAX_S6);
    if sigma == LOOKUP_SIGMA_MAX_S6 {
        return table[LOOKUP_N_SIGMA - 1];
    }
    let offset = sigma - LOOKUP_SIGMA_MIN_S6;
    let ix = offset * LOOKUP_N1;
    let i0 = (ix / SIGMA_RANGE_S6).min(LOOKUP_N_SIGMA as i64 - 2) as i32;
    let frac = (((ix % SIGMA_RANGE_S6) * WN as i64) / SIGMA_RANGE_S6) as usize;
    let weight = &CR_W[frac.min(WN - 1)];
    let n = LOOKUP_N_SIGMA as i32;
    let p0 = table[(i0 - 1).clamp(0, n - 1) as usize];
    let p1 = table[i0.clamp(0, n - 1) as usize];
    let p2 = table[(i0 + 1).clamp(0, n - 1) as usize];
    let p3 = table[(i0 + 2).clamp(0, n - 1) as usize];
    cr_dot(weight, p0, p1, p2, p3)
}

#[inline(always)]
pub fn lookup_table_linear(table: &[i64; 64], sigma_s6: i64) -> i64 {
    let sigma = sigma_s6.clamp(LOOKUP_SIGMA_MIN_S6, LOOKUP_SIGMA_MAX_S6);
    if sigma == LOOKUP_SIGMA_MAX_S6 {
        return table[LOOKUP_N_SIGMA - 1];
    }
    let offset = sigma - LOOKUP_SIGMA_MIN_S6;
    let ix = offset * LOOKUP_N1;
    let i0 = (ix / SIGMA_RANGE_S6).min(LOOKUP_N_SIGMA as i64 - 2) as usize;
    let rem = ix % SIGMA_RANGE_S6;
    let p1 = table[i0];
    let p2 = table[i0 + 1];
    p1 + ((p2 - p1) * rem + SIGMA_RANGE_S6 / 2) / SIGMA_RANGE_S6
}
