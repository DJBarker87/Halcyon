//! Precomputed obs1 seed tables for the projected-filter autocall pricer.
//!
//! Raw tables: unprojected safe_w and knocked_w per GH-13 node.
//! Projected tables: K=9 projected FilterState for safe and knocked,
//! precomputed so that on-chain we skip the 13-node reconstruction
//! and project_state calls.
//!
//! Anyone can regenerate by running:
//! `cargo run -p halcyon-quote --bin gen_obs1_seed_tables`

use crate::nig_weights_lookup::{
    NIG_WEIGHT_N_SIGMA, NIG_WEIGHT_SIGMA_MAX_S6, NIG_WEIGHT_SIGMA_MIN_S6,
};
use crate::worst_of_c1_filter::{FilterNode, FilterState, MAX_K};

const SIGMA_RANGE_S6: i64 = NIG_WEIGHT_SIGMA_MAX_S6 - NIG_WEIGHT_SIGMA_MIN_S6;
const N1: i64 = (NIG_WEIGHT_N_SIGMA - 1) as i64;
const N_RAW_NODES: usize = 13;
const N_PROJ_NODES: usize = 9;
const WN: usize = 1024;
const WS: u32 = 30;

const CR_W: [[i32; 4]; WN] = {
    let mut out = [[0i32; 4]; WN];
    let scale: i64 = 1 << WS;
    let mut k = 0usize;
    while k < WN {
        let t = (k as i64) << (WS - 10);
        let t2 = t * t >> WS;
        let t3 = t2 * t >> WS;
        out[k][0] = ((-t + 2 * t2 - t3) / 2) as i32;
        out[k][1] = ((2 * scale - 5 * t2 + 3 * t3) / 2) as i32;
        out[k][2] = ((t + 4 * t2 - 3 * t3) / 2) as i32;
        out[k][3] = ((-t2 + t3) / 2) as i32;
        k += 1;
    }
    out
};

#[inline(always)]
fn cr_dot(w: &[i32; 4], p0: i64, p1: i64, p2: i64, p3: i64) -> i64 {
    (w[0] as i64 * p0 + w[1] as i64 * p1 + w[2] as i64 * p2 + w[3] as i64 * p3) >> WS
}

#[inline(always)]
fn obs1_lookup(table: &[i64; 64], sigma_s6: i64) -> i64 {
    let sigma = sigma_s6.clamp(NIG_WEIGHT_SIGMA_MIN_S6, NIG_WEIGHT_SIGMA_MAX_S6);
    let offset = sigma - NIG_WEIGHT_SIGMA_MIN_S6;
    let ix = offset * N1;
    let i0 = (ix / SIGMA_RANGE_S6).min(NIG_WEIGHT_N_SIGMA as i64 - 2) as i32;
    let frac = (((ix % SIGMA_RANGE_S6) * WN as i64) / SIGMA_RANGE_S6) as usize;
    let weight = &CR_W[frac.min(WN - 1)];
    let n = NIG_WEIGHT_N_SIGMA as i32;
    let p0 = table[(i0 - 1).clamp(0, n - 1) as usize];
    let p1 = table[i0.clamp(0, n - 1) as usize];
    let p2 = table[(i0 + 1).clamp(0, n - 1) as usize];
    let p3 = table[(i0 + 2).clamp(0, n - 1) as usize];
    cr_dot(weight, p0, p1, p2, p3)
}

