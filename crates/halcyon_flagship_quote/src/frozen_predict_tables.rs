//! Precomputed frozen predict-state grid tables.
//!
//! Stores grid_min and grid_span. Linear interpolation in sigma.
//! Uniform K=7,9,12,15 plus tapered [15,9,5,3,3].
//!
//! Regenerate: `cargo run -p halcyon-quote --bin gen_frozen_predict_tables`

use crate::worst_of_c1_filter::MAX_K;

const FP_SIGMA_MIN_S6: i64 = 50000;
const FP_SIGMA_MAX_S6: i64 = 1000000;
const FP_N_SIGMA: usize = 128;
const FP_SIGMA_RANGE: i64 = FP_SIGMA_MAX_S6 - FP_SIGMA_MIN_S6;
const FP_N1: i64 = (FP_N_SIGMA - 1) as i64;
const N_OBS_PREDICT: usize = 5;

pub const TAPERED_K_SCHEDULE: [usize; 5] = [15, 9, 5, 3, 3];

#[inline(always)]
fn fp_lerp(table: &[i64; FP_N_SIGMA], sigma_s6: i64) -> i64 {
    let sigma = sigma_s6.clamp(FP_SIGMA_MIN_S6, FP_SIGMA_MAX_S6);
    let offset = sigma - FP_SIGMA_MIN_S6;
    let scaled = offset * FP_N1;
    let i0 = (scaled / FP_SIGMA_RANGE) as usize;
    let i0 = if i0 >= FP_N_SIGMA - 1 {
        FP_N_SIGMA - 2
    } else {
        i0
    };
    let frac_num = scaled - i0 as i64 * FP_SIGMA_RANGE;
    let v0 = table[i0];
    let v1 = table[i0 + 1];
    v0 + (v1 - v0) * frac_num / FP_SIGMA_RANGE
}

