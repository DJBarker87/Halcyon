/// Look up raw obs1 seed weights via Catmull-Rom interpolation.
/// Returns (safe_w[13], knocked_w[13], first_hit, first_knock_in).
pub fn obs1_raw_weight_lookup(sigma_s6: i64) -> ([i64; N_RAW_NODES], [i64; N_RAW_NODES], i64, i64) {
    let mut sw = [0i64; N_RAW_NODES];
    let mut kw = [0i64; N_RAW_NODES];
    let mut idx = 0;
    while idx < N_RAW_NODES {
        sw[idx] = obs1_lookup(&OBS1_SAFE_W[idx], sigma_s6).max(0);
        kw[idx] = obs1_lookup(&OBS1_KNOCKED_W[idx], sigma_s6).max(0);
        idx += 1;
    }
    let fh = obs1_lookup(&OBS1_FIRST_HIT, sigma_s6).max(0);
    let fki = obs1_lookup(&OBS1_FIRST_KNOCK_IN, sigma_s6).max(0);
    (sw, kw, fh, fki)
}

