// Catmull-Rom interpolation in σ — same N sample points as the table.
// Smooth across knots (C¹ continuity), eliminates the linear-interp
// kinks that produced the off-anchor monotonicity violations seen in
// the production sweep. ~50 CU extra vs linear.
const WN: usize = 1024;
const WS: u32 = 30;

const CR_W: [[i32; 4]; WN] = {
    let mut out = [[0i32; 4]; WN];
    let scale: i64 = 1 << WS;
    let mut k = 0usize;
    while k < WN {
        let t = (k as i64) << (WS - 10);
        let t2 = t * t >> WS;
        let t3 = t2 * t >> WS;
        out[k][0] = ((-t + 2 * t2 - t3) / 2) as i32;
        out[k][1] = ((2 * scale - 5 * t2 + 3 * t3) / 2) as i32;
        out[k][2] = ((t + 4 * t2 - 3 * t3) / 2) as i32;
        out[k][3] = ((-t2 + t3) / 2) as i32;
        k += 1;
    }
    out
};

#[inline(always)]
fn cr_dot(w: &[i32; 4], p0: i64, p1: i64, p2: i64, p3: i64) -> i64 {
    (w[0] as i64 * p0 + w[1] as i64 * p1 + w[2] as i64 * p2 + w[3] as i64 * p3) >> WS
}

/// Look up K=12 correction at a given sigma via Catmull-Rom on the
/// N sample points. Returns micro-bps.
/// Add `result as f64 / 1e6` to fair_coupon_bps to apply.
///
/// Endpoint clamping: at boundary cells, missing p0/p3 reflect the
/// nearest interior value (no extrapolation overshoot).
#[inline(always)]
pub fn k12_correction_lookup(sigma_s6: i64) -> i64 {
    let sigma = sigma_s6.clamp(SIGMA_MIN_S6, SIGMA_MAX_S6);
    let offset = sigma - SIGMA_MIN_S6;
    let scaled = offset * N1;
    let i0 = (scaled / SIGMA_RANGE_S6) as usize;
    let i0 = if i0 >= N - 1 { N - 2 } else { i0 };
    let frac_num = scaled - i0 as i64 * SIGMA_RANGE_S6;
    let wi = ((frac_num as i128 * WN as i128 / SIGMA_RANGE_S6 as i128) as usize).min(WN - 1);
    let w = &CR_W[wi];
    let n = N as i64;
    let p0 = K12_CORRECTION[((i0 as i64) - 1).clamp(0, n - 1) as usize];
    let p1 = K12_CORRECTION[(i0 as i64).clamp(0, n - 1) as usize];
    let p2 = K12_CORRECTION[((i0 as i64) + 1).clamp(0, n - 1) as usize];
    let p3 = K12_CORRECTION[((i0 as i64) + 2).clamp(0, n - 1) as usize];
    cr_dot(w, p0, p1, p2, p3)
}
